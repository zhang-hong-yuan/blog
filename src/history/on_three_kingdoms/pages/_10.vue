	<p>易中天：</p>
	<p>
		上一集我们讲了官渡之战，官渡之战的结局大家现在也都知道了，就是曹操以弱胜强，以寡敌众，大获全胜。这个结局其实早在袁绍的两个谋士沮授和田丰的预料之中，因此沮授和田丰当时坚决反对袁绍发动这样一场战争，其结果是沮授被曹操俘虏，田丰被袁绍关进了大狱。而且我们上一集讲到，当袁绍集团战败的消息传到邺城的时候，朋友们去看望田丰，说田兄这一下可就有出头之日了，而田丰的回答是我这一会是死定了。
