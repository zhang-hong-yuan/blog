	<p>
		刘备夺蜀成功的第四个原因，是曹操失误。因为刘备夺蜀花了三年功夫，三年期间曹操对刘备居然没有任何动作，他的心思都在别的地方在忙别的事情，而且还在这个期间打了一次孙权，结果是曹孙鹬蚌相争，刘备渔翁得利。所以我说刘备的入蜀是乘虚而入，这个“虚”包括两个方面，一个是刘璋的懦弱，他的虚，还有一个是整个三国的战场上，这儿是一个空挡，所以是乘虚而入。但是除了这些客观原因以外，刘备的这个成功也有他的原因。
