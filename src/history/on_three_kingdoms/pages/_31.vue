	<p>
		这个事情呢，如何评价，这个也有不同的意见，但是我比较倾向于赞成张作耀先生在《刘备传》里的看法，就是刘备夺取益州，夺取蜀郡的这一整套做法从道德上是无可恭维，但是从当时的情势来讲也无可指责。刘璋蒙辱投降，避免了蜀地人民的苦难，还是应该肯定，我们可以说他懦弱无能，但是不能说他骨头不硬，不要讥笑他。
