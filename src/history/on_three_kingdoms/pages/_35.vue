	</p>
	<p>那么，刘备究竟是如何指挥这场战争的呢？面对来势汹汹的刘备，东吴孙权一方又是如何应对的呢？厦门大学易中天教授将为您精彩讲述《易中天品三国》之“夷陵之战”。</p>
