	<h1>必争之地</h1>
	<p>
		荆州，汉代十三州当中最大的州之一，东汉末年，各路诸侯都对这块战略要地垂涎三尺，因为他们认为，荆州的拥有者刘表没有能力，守不住荆州。作为镇守一地的最高长官，刘表有那么窝囊吗？他究竟是一个怎样的人呢？厦门大学易中天教授做客《百家讲坛》，为您精彩品三国之——必争之地。
	</p>
	<p>
		荆州是东汉末年群雄争霸的战略要地，而荆州的拥有者刘表在各路诸侯的眼中，又是一个没有多少能力，只想保住自己一亩三分地的庸碌之辈，于是诸侯们各施手段争夺荆州。这种争夺改变了当时中国的状态，争夺的结果，最终奠定了三国鼎立的基础。那么，荆州的主人刘表，在诸侯争霸的夹缝中，究竟在荆州做了些什么？他有哪些功过得失？他究竟又是一个怎样的人呢？厦门大学易中天教授做客《百家讲坛》，为您精彩品三国之“必争之地”。
	</p>
	<p>
		在前面几集我们讲到，荆州这个地方被鲁肃、诸葛亮也包括曹操定为必争之地，就是鲁肃为孙权做的规划也好，诸葛亮为刘备做的规划也好，还是曹操自己为自己做的规划也好，荆州这个地方是一定要拿下的。为什么要争，前面已然讲过，今天我们就来讲它为什么能争。为什么能争呢？诸葛亮在《隆中对》里面说得非常清楚：“其主人不能守”，也就是荆州这块地方当时的占有者刘表，他守不住荆州，那么刘表又为什么守不住荆州呢？那我们就要来看看刘表是一个什么样的人？
	</p>
	<p>
		在《后汉书》里面，刘表是和袁绍合为一传的。我们知道从《史记》到《汉书》到《三国志》到《后汉书》，它都是纪传体的史书，这些历史学家在为传主做传的时候往往会把某一些人合为一传，而哪些人合为一传呢？那么这些合为一传的人，他们相互之间是有某种关联的。那么《后汉书》把袁绍和刘表合为一传，说明袁绍和刘表在他看来是同一类型的人，是什么人呢？如果说曹操是可爱的奸雄，那么袁绍和刘表就可以说是漂亮的草包。
	</p>
	<p>
		我们先来看看刘表的漂亮，刘表这个人可以用16个字来总结，就是帝室之胄、一表人才、党锢中人、乱世名流。据《三国志》和《后汉书》，我们知道刘表他是鲁公王之后，是地地道道的宗室，而且刘表的这个宗室的身份看起来还是比较靠得住的，不像刘备的那个皇叔身份可能是注水猪肉。而且刘表身“长八尺馀，姿貌甚伟”，我们知道诸葛亮身长八尺，合现在的一米八四，刘表身长“八尺馀”，那至少也有一米八六，甚至一米八八，而且相貌是很漂亮的。他在东汉末年是士人当中的名流，我们知道东汉末年士人集团和宦官集团这个斗争很激烈，后来宦官集团得势的时候曾经禁锢了一批士人，也称为党人，这叫“党锢中人”，刘表是其中这一，遭到当时朝廷的追捕，因为跑得快，幸免于难。那这样后来宦官集团失败了以后，刘表翻身了，所以他名声很好，他名声之好，上了当时的排行榜，叫做“八顾”，什么叫“顾”呢？就是有德行的人，当时品评人物，评出了八个最有德行的人，刘表是其中之一，所以这是一个漂亮人物，而这些在当时都是刘表成就一番事业的资本。现在我们看起来觉得会很可笑，说一个人能不能成就一番事业与长得怎么样有什么关系呢？在当时还是讲究这个的。
	</p>
	<p>
		＊在讲究出身门第，讲究相貌和品行的士人阶层中，刘表的优点是突出的，他在治理荆州的时候，也使荆州在一定时期内获得了安定和平，这应该说是他能力的具体体现。但是诸葛亮却认为刘表守不住荆州，而曹操集团的郭嘉、孙权集团的鲁素在规划荆州的时候，也都没有看重刘表的作用，那么，刘表真的是中看不中用吗？他到底有没有本事呢？
	</p>
	<p>
		刘表也有本事。汉献帝初平元年，也就是公元190年这个时候，长沙太守孙坚杀了荆州刺史王睿，这个事情我们在《江东基业》这一集已经交待过了，那么荆州刺史这个职位就出缺了，朝廷就任命刘表担任荆州刺史。但是当时荆州是非常地混乱，怎样混乱呢？一个是袁术，包括袁术手下的孙坚，图谋荆州；第二个就是荆州当地有所谓宗贼作乱，什么叫宗贼呢？就是按照家族和宗法关系组织起来的地方性非政府武装。结果刘表不能到任，也就是说刘表虽然得到了朝廷的任命，实际上是没有办法治理荆州这个地方的。这个时候刘表就采取了一个正确的措施，依靠当地的豪族、豪门世族来平定荆州，他找到了两个人，一个叫做蒯越，一个叫做蔡瑁。蒯越是一个非常有能力的人，蒯越字异度，连曹操都非常欣赏他，后来曹操拿下荆州以后，写信回去说我高兴的不是得到了荆州，是得到了异度，得到了蒯越。蔡瑁是荆州的一个豪门，非常地富有。
	</p>
	<p>
		刘表找到这两个人，说你看我现在应该怎么办呢？蒯越说了这一番话，蒯越说：“理平者先仁义，理乱者先权谋”，就是你要治理一个太平盛世应该怎么样呢？要靠道德的力量，靠道德的感召，靠道德的教化，要靠仁义；那么你要治理一个动乱的地方该怎么办呢？那就要使用权术和谋略，要动用武力的威胁。而现在使君你面临的，一方面是袁术的入侵，另一方面是宗贼的作乱，应该怎么样？先把宗贼摆平。蒯越给他出主意，八个字，叫“诛其无道，施其才用”，什么意思呢？就把那个宗贼的头目当中这个最坏的，民愤最大的，作恶多端的，把他杀了，然后把宗贼当中的一些人才你把他招聘了，软硬兼施。刘表采纳了蒯越的建议，结果“江南悉平”，刘表由刺史变成了州牧，说明他在荆州这个地方成功了。
	</p>
	<p>
		成功以后的刘表表现出来也很大度，当时张绣的叔叔张济，任骠骑将军在关中，因为没有军粮，就带着部队入侵荆州，攻打穰城，结果中箭身亡。这个时候荆州的大小官员都跑来跟刘表祝贺，恭喜州牧，张济跑来打我们，你看现在死了，很高兴。刘表说什么呢？刘表说：“济以穷来，主人无礼，至于交锋。”说张济是因为没有饭吃，走投无路才到我们荆州来，我们没有礼貌啊，我们没有以礼相遇啊，还打起来了，“此非牧意，牧受吊，不受贺也。”这不是我们初衷，就是我接受吊唁，不接受祝贺。那么这个话一说出来以后，大家觉得刘表这个人真是个正人君子，真是仁义道德，张济的部下都对刘表表示服从，表示钦佩。刘表又安顿了张济的侄子张绣驻扎在宛城，就是现在河南省的南阳市，而且供应他军粮，和他联合起来对付曹操。这样到了建安三年，刘表拥有了荆州七郡，“地方数千里，带甲十余万，万里肃清，大小咸悦而服之。”也就是这个境内大大小小的势力都服了刘表，刘表这个时候做什么呢？建学校，兴儒术。抚士人。
	</p>
	<p>
		＊刘表治理荆州，采用权术和谋略并重的策略，不仅很快站稳了脚跟，还把荆州治理得井井有条，◆这样看起来，刘表不仅是人长得漂亮，事情做得也很漂亮，应该说是取得了一定的成就。那么，为什么历史上对刘表的评价并不高呢？关于这件事，易中天先生是怎样分析的呢？
	</p>
	<p>
		有以下几个原因：第一，刘表胸无大志。郭嘉就曾经很不屑地说刘表：“表，坐谈客耳”，刘表这个人是个夸夸其谈的，开开座谈会可以的，干什么大事不行的。曹操也非常不屑地说刘表，“自守之贼也”，这是一个只能守住自己小摊子的一个贼。为什么呢？刘表他确实是没有统一中国、获得天下的这样的一个志向，他的愿望就是老婆、孩子、热炕头，一亩三分地，把它守好，经营好了，他就心安理得了，这是他第一点。
	</p>
	<p>
		第二点，刘表这个人也没有肚量。当时官渡之战的时候，袁绍和曹操在官渡相持不下，袁绍派人去联合刘表，刘表满口答应，但不出兵，他也不帮助曹操，他坐山观虎斗。这个时候刘表的部下韩嵩等人就来劝刘表了，韩嵩他们说什么呢？他们说：“豪杰并争，两雄相持，天下之重在于将军。”现在曹操和袁绍这两个巨头正在一决雌雄的时候，这个天下形式的权重就在将军您身上了，就像当年刘邦和项羽决战的时候，下旗七十二城的韩信就成了天下的权重，这个时候你刘表往袁绍这边一站，袁绍就赢了，你往曹操那边一站，曹操就赢了。韩嵩说在这样的形势下，你应该怎么样呢？或者趁火打劫，或者投靠一方，那他俩不是打起来了吗，你最好的办法就是趁机捞一把，至少你要表个态，你要站在谁那边，为什么要表态呢？像你这样，“见贤而不能助，请和而不得”，见到该支持的你不去支持他，你不支持也可以，你劝他们和解吧，你又劝不了，等他们这一仗打完了，双方的怨恨都集中在你身上，你是两头不讨好，两边都要恨你，“此两怨必集于将军，将军不得中立矣。”你想中立呀，你守不住了。这个时候什么蒯越这些人都来劝刘表，而且他们的主张是什么呢？也很明确，支持曹操，说袁绍这个人不灵的，得不了天下的，得天下的肯定是曹操，现在曹操又弱，你赶快趁机支持他一把。
	</p>
	<p>
		刘表想来想去，就跟韩嵩说：要不这样，你到北方去看看，看看情况。韩嵩说：将军啊，请你想好了，如果你打算投靠曹操，韩嵩是可以去的，如果你不打算投靠曹操，韩嵩就不能去，为什么呢？因为曹操是奉天子以令不臣，现在我们万岁爷是在曹操那儿，曹操不是代表他个人，他代表的是朝廷啊，韩嵩从荆州出使朝廷，朝廷多半会给韩嵩一官半职，韩嵩能够把它推辞掉当然没有问题，如果韩嵩推辞不掉，接受了天子的委任，那么我韩嵩从此就是皇上的人，不是将军你的人。韩嵩说得很清楚：“在君为君，不复为将军死也。”那我们关系就变了，我是天子的新臣，将军的故吏，我们只是朋友关系了，没有这个从属关系了，你要想好啊。刘表说你这什么意思嘛？你是不是不敢去啊，去。韩嵩到了北方，到了许都，见了曹操，又见了皇帝，被任命为侍中零陵太守，韩嵩回来以后就说朝廷朝廷怎么好，曹操曹操怎么好，皇帝皇帝怎么好，净说皇帝、曹操、朝廷的好话，刘表勃然大怒，你这小子背叛我啊，杀了。韩嵩说：你不应该杀我啊，你我有言在先啊，将军记不记得我临走之前跟你说了什么话啊。刘表的老婆蔡氏这个时候也来劝刘表，说韩嵩可是个人才，杀不得的，刘表还是很生气，最后把韩嵩关起来，直到后来曹操拿下荆州以后，韩嵩才出狱。所以这个事情陈寿有个评价，说刘表这个人“外貌儒雅，而心多猜忌”，这是第二点。
	</p>
	<p>
		第三点，刘表不会用人。前面讲过荆州这个地方是很有些人才的，别的不说，刘备、诸葛亮，刘备天下枭雄，诸葛亮人中之龙，都在刘表身边，刘表都不能用，都视而不见。北方逃难来的那些士人，只见刘表安顿，不见刘表重用，尤其是对于刘备这个人，刘表是表面上客客气气的，暗中时刻提防，《三国志》的说法是“疑其心，阴御之”，是的防刘备的。甚至按照《三国志》裴松之注所引《世语》这本书，说刘表还摆了一个鸿门宴，刘表的手下蔡瑁这些人还准备在席间对刘备下手，是刘备假装上厕所，逃了出去，逃了出去以后，刘备骑了一匹马叫“的卢”，一路跑，跑到檀溪，马掉到水里面不能动，刘备急了说，的卢啊，的卢啊，今天你要努力一下啦！然后的卢马一跃三丈，跃过檀溪，逃得性命。后来《三国演义》写了一回叫做“刘皇叔跃马过檀溪”，有这么一个故事，但这个事情孙盛认为是不可能的，孙盛说如果刘表想杀刘备的话，刘备不可能安然无恙地在荆州待了六年，我也认为这个是不大可能，但是提防他恐怕是事实。
	</p>
	<p>
		那么刘表的第四个问题，是后继无人。说他后继无人不是说他没有儿子，而是说他的儿子不中用，他安排又不恰当。刘表有两个儿子，大儿子叫刘琦，小儿子叫刘琮，这两个儿子都是刘表的前妻所生，而且刘表本来是喜欢大儿子的，但是他这个后妻喜欢小儿子，为什么呢？因为他的后妻蔡氏把自己娘家的侄女许配给了刘琦，这样就亲上加亲了。这样一来呢，刘表后妻蔡氏背后的这个集团，就是以蔡瑁为代表的这样的一个荆州地方豪门士族集团，就站在了刘琮的一边，然后他们都到刘表那儿，去说刘琦的坏话。刘表这个人是个什么样的人呢？借用一句成都话说叫做“耳朵”，啥子叫“耳朵”呢？就是怕老婆的人，耳朵软，“”是什么东西呢？“”就是一个东西，一种食品，经过加工以后外形不变，里面软了，叫做“”。比方说一只红薯，是个完整的，你放到炉子里一烤，把它烤软了，叫“红薯”。耳朵它是完整的，但是软，这个枕头上风一吹，他就糊涂了，他就喜欢小儿子刘琮了，这刘琦就紧张了，刘琦就想到了一个人，他认为这个人是应该可以帮助他，这个人是谁呢？这个人就是诸葛亮。
	</p>
	<p>
		诸葛亮何等聪明的人，这点小事情还摆不平吗？于是刘琦就去找诸葛亮，姐夫啊，帮个忙吧。诸葛亮再三地推辞，为什么呢？诸葛亮不好说，论身份，诸葛亮现在已经是刘备的部下，因此他也是刘表的部下；论辈份，刘表现在的妻子是刘琮和刘琦的后母，而刘表的这个妻子和诸葛亮的丈母娘是姐妹，因此，刘琮也好，刘琦也好，是诸葛亮的后表舅子，所以他帮谁都不合适啊，不好搀和到这种事情里面去。刘琦没有办法，他就跟诸葛亮说，哥啊，我那个园子里面有些宝贝，你来看看，把诸葛亮请到他园子里面，说在楼上。两个人上楼上，刘琦就下命令，把梯子撤了，然后跪下来说：现在我们是上不着天，下不着低，这个范围内只有我们两个人，你的话从你的嘴里出来，到我的耳朵里，就烂在我肚子里面了，没有人知道，可以说了吧？唉，诸葛亮说，那这样吧，他说你知道重耳和申生的故事吗？重耳和申生是什么呢？晋献公的儿子，是晋献公前妻所生，申生是太子，重耳是公子。晋献公后来娶了骊姬，宠爱这个后妻骊姬，想立骊姬的儿子奚齐为太子，就要谋害这两个儿子，结果怎么样呢？结果申生留在晋国不走，最后被谋杀，重耳逃出去，保全了性命，最后回国来当了国君，就是晋文公。这么一说，刘琦恍然大悟，哦，明白了。
	</p>
	<p>
		正好这个时候江夏太守黄祖被孙权杀了，江夏太守没人了，刘琦就去找他爸说那我去做夏太守。好，你去吧，刘表批准了。刘琦带了一万人到了夏口，就是现在的汉口，武汉市的汉口，做了江夏太守。所以这个历史有时候非常好玩儿，以诸葛亮经天纬地之才，初出茅庐第一计，是给人家处理的家务事。但是为刘备立了一大功，因为刘琦带走的这一万人是后来赤壁之战中刘备一半的本钱，因为赤壁之战刘备两万人，一万是关羽的水军，还有一万就是刘琦的兵。
	</p>
	<p>
		刘表的这个后事安排是不恰当的，它为后来荆州的分裂打下了基础，甚至可以说自从刘表做出让刘琮接班，把刘琦派出去当江夏太守的这个决意以后，荆州集团在事实上已经分裂成两派，一派以刘琮为名义上的代表，背后是蔡瑁、蒯越这些人。蒯越我们前面讲了是曹操欣赏的人，蔡瑁估计与曹操有旧，跟曹操老早就认识了，后来曹操得到荆州以后，还进了蔡瑁的家，到了他的内室，见到了蔡瑁的妻子，可见曹操和蔡瑁的关系是很好的，那么这一派我们可以叫做降曹派。
	</p>
	<p>
		另一派以刘琦为名义上的代表——这俩人都只能当名义上的代表，这俩人没用的，——背后是谁呢？背后是刘备、诸葛亮。刘备和诸葛亮的政治态度也是很清楚的，刘备是决心要抗曹的，因为刘备不可能投降曹操，他是从曹操那儿背叛跑出来的，而且曹操已经说过了“天下英雄唯使君与操耳”，刘备投降曹操他能有什么好下场，他只有死硬到底。而诸葛亮在《隆中对》里面已经都规划好了，就是要和曹操、孙权三分天下，也不会主张刘备去投降曹操，所这一派我们无妨称之为抗曹派。
	</p>
	<p>
		刘表是什么态度呢？刘表是既不想降曹，也不想联吴，他想守中立，他想保住他这一亩三分地。但是他管得了自己，他管不了儿子；管得了生前，他管不了身后。而对他的后事他居然做出这样一个安排来，岂非是草包。所以陈寿对刘表这个人的评价不高，他也是把刘表和袁绍放在一起评论的，他说刘表和袁绍这两个人的共同特点是有仪表，人长得漂亮；有风度，儒雅；第三，有名气，名声很大；第四，也有成就，我讲过袁绍他的成就已经超过了他的祖辈和父辈，刘表到荆州以后由单骑入宜城到一统荆州，也是有成就的。但是他们共同的特点是“外宽内忌，好谋无决，有才而不能用，闻善而不能纳，废嫡立庶，舍礼崇爱。”陈寿说这是这两人的共同特点，所以说是漂亮的草包。
	</p>
	<p>
		＊刘表虽然凭借权术和谋略在荆州暂时站稳了脚跟，但他有限的权术和谋略并不能帮助他在荆州长久地站住脚跟，因为他胸无大志，心胸狭窄，再加上一些错误的决策，最终为荆州的分裂埋下了伏笔。而袁绍也是心胸狭窄之人，也做出过很多错误的决策，最终也导致了失败的结局。那么，刘表和袁绍除了这些相同点之外，他们究竟有什么不一样的地方呢？
	</p>
	<p>
		在这里我还是要给刘表说句公道话，刘表和袁绍还不能说是完全一样。第一，袁绍是自取灭亡，刘表是在劫难逃。袁绍是自己去惹曹操的，官渡之战是袁绍发动的，刘表没有发动过战争啊，他是个“自守这贼”，他只是想守住自己这块地盘，他是因为荆州变成了必争之地，他在劫难逃啊，第一点。
	</p>
	<p>第二点，以刘表的能耐，他除了做一个“自守这贼”以外，他也别无出路，他没有能力进攻。他的错误只在于不明白守有两种守法，其中之一叫做以攻为守，他应该采取以攻为守的策略，他可能荆州就守住了，这是第二点。</p>
	<p>第三点，正由于刘表实行了“爱民养士，从容自保”的政策，在他的治理下荆州地区获得了十多年的安定和和平，荆州地区的人民和士人过着安定和较为幸福的生活，这是应该肯定的。而且刘表是有自知之明的，他是想苟全荆州于乱世。</p>
	<p>
		所以说刘表是漂亮的草包可能过了一点，不太恰当，比较准确的评价是历史学家何兹全先生提出来的，他说如果说曹操是“治世之能臣，乱世之奸雄”，那么刘表就是“治世之贤臣，乱世之庸人”。刘表最大的不幸是不该生在这个弱肉强食的乱世，这不是他的错误。而且刘表也不是一点运气都没有，刘有也还是有运气的，他被朝廷派遣到荆州，就是他的运气。因为荆州这个地方是当时汉代十三州当中最大的州之一，汉代十三个州有两个州是最大的，一个是荆州，一个是益州。这个地方很大，也很富庶，但不是政治中心，因此争夺天下的战争首先不发生在荆州地区，发生在北方，袁绍和曹操的这个争夺天下的战争它就是在北方打的。所以在天下未定之时，荆州不失为一个避乱和苟全性命的好地方，但是到了天下将定的时候，荆州就成了虎狼眼中必须争夺的一只羊，所以刘表是不幸的幸运儿。当然刘表是有自知之明的，他知道在这样一个弱肉强食的乱世，他只能采取苟全荆州于乱世的政策，但是树欲静，而风不止，曹操早就在打荆州的主意，诸葛亮和鲁肃为各自的君主所做的战略规划也盯准了荆州，那么荆州的命运将如何呢？请看下集——兵临城下。
