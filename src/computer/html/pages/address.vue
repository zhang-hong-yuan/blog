	<p>块级元素</p>

	<p>【&lt;address&gt;】标签定义文档作者/所有者的联系信息。【&lt;address&gt;】 元素通常被包含在 【&lt;footer&gt;】 元素的其他信息中。</p>
	<ul>
		<li>如果 【&lt;address&gt;】 元素位于 【&lt;body&gt;】 元素内部，则它表示该文档作者/所有者的联系信息。</li>
		<li>如果 【&lt;address&gt;】 元素位于 【&lt;article&gt;】 元素内部，则它表示该文章作者/所有者的联系信息。</li>

	</ul>
	<p>不应该使用 【&lt;address&gt;】 标签来描述邮政地址，除非这些信息是联系信息的组成部分。</p>
	<p>【&lt;address&gt;】元素的文本通常呈现为<em>斜体</em>。大多数浏览器会在该元素的前后添加换行。</p>
