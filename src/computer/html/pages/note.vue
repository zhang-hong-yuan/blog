	<h1>HTML 注释 <span>&lt;!--...--&gt;</span></h1>


	<p>HTML 拥有在代码中写注释的机制。浏览器会忽略注释，有效地使注释对用户来说不可见。</p>

	<p>注释的目的是让开发者在代码中加入注释，以解释代码的逻辑或编码。如果开发者在离开很久后回到一个代码库，以至于不能完全记住它，这就非常有用。</p>


	<p>为了将一段 HTML 中的内容置为注释，需要将其用特殊的记号 ‘&lt;!--’ 和 ‘--&gt;’ 包裹起来，比如：</p>

	<pre>
	<code v-code:html.escape>
<p>我在注释外！</p>

<!-- <p>我在注释内！</p> -->
	</code>
</pre>


	<p>注意：注释标记不可嵌套。</p>
</template>