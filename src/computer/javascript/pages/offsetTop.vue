	<h1>HTMLElement.offsetTop</h1>


	<p>‘HTMLElement.offsetTop’ 为只读属性，它返回当前元素相对于其 ‘offsetParent’ 元素的顶部内边距的距离。</p>


	<pre>
		<code v-code:js.nocopy>
			topPos = element.offsetTop;
		</code>
	</pre>

	<dl>
		<dt>‘topPos’</dt>
		<dd>为返回的像素数。</dd>
	</dl>




	<hr>



	<h1>HTMLElement.offsetLeft</h1>


	<p>‘HTMLElement.offsetLeft’ 是一个只读属性，返回当前元素左上角相对于 ‘HTMLElement.offsetParent’ 节点的左边界偏移的像素值。</p>


	<pre>
		<code v-code:js>left = element.offsetLeft;</code>
	</pre>

	<dl>
		<dt>‘left’</dt>
		<dd>是一个整数，表示向左偏移的像素值。</dd>
	</dl>



