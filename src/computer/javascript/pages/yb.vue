<template>
	<h1>小括号/函数调用符【()】</h1>

	<p>圆括号运算符【( )】用于控制表达式中的运算优先级。</p>

	<pre v-code:js class="nocopy">(expression)</pre>
	<p>圆括号运算符由一对圆括号组成，包裹表达式和子表达式用来覆盖常规的运算符优先级，达到低优先级的表达式比高优先级的表达式更早运算。</p>


	<h6>示例：</h6>

	<p>下面的代码展示了加法运算先于乘法运算的情况。</p>

	<pre v-code:js>
const a = 1;
const b = 2;
const c = 3;

// 默认优先级
a + b * c; // 7
// 默认运算顺序同下
a + (b * c); // 7

// 现在优先考虑乘法之前的加法
(a + b) * c; // 9

// 这相当于
a * c + b * c; // 9
</pre>
</template>