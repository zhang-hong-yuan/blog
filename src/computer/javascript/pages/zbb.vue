	<h1>三元表达式</h1>
	<p>三元表达式也能做一些简单的条件选择。有三元运算符组成的式子称为三元表达式。</p>
	<pre v-code:js>表达式1 ? 表达式2 : 表达式3;</pre>
	<p>如果表达式1为 true ，则返回表达式2的值；如果表达式1为 false，则返回表达式3的值。</p>
	<p>若三元表达式结果为布尔值且其中一个值为 false，则可用逻辑中断或 【||】 取代。</p>
	<p>三元操作符是右结合的，也就是说可以把它链接起来， 和 【if ... else if ... else if ... else】 链类似。</p>
</template>